  <section class="section">
    <h1>What is Procurement?</h1>

    <p>
      Procurement in project management refers to the process of acquiring goods, services, or works from external sources to fulfill project requirements. It is one of the key knowledge areas defined by the Project Management Institute (PMI) and plays a vital role in determining the overall success of a project.
    </p>
    <p>
      The procurement process spans the entire project lifecycle — from identifying what needs to be purchased, to selecting the right vendors, negotiating contracts, managing delivery, and closing out agreements. When done well, it ensures that the right resources arrive at the right time, at the right cost, and at the right quality.
    </p>
    <p>
      Whether you're outsourcing development work, purchasing hardware, or contracting specialized consultants, understanding how procurement works gives project managers a significant advantage in keeping their projects on track and within budget.
    </p>
  </section>

  <!-- SECTION 2: Importance -->
  <section class="section">
    <h2>Why Procurement Matters to Project Managers</h2>

    <p>Procurement is far more than just purchasing — it directly affects the project's scope, timeline, budget, and quality. Here's why it matters:</p>

    <ul class="importance-list">
      <li><strong>Cost Control</strong> — Strategic procurement can significantly reduce project costs through competitive bidding, volume discounts, and smart contract negotiations.</li>
      <li><strong>Risk Management</strong> — Proper contract types and vendor selection help transfer or mitigate risks that would otherwise fall on the project team.</li>
      <li><strong>Quality Assurance</strong> — Clear procurement specifications and evaluation criteria ensure that deliverables meet the required standards.</li>
      <li><strong>Timeline Protection</strong> — Well-managed procurement prevents delays caused by late deliveries, scope misunderstandings, or vendor underperformance.</li>
      <li><strong>Legal Compliance</strong> — Structured procurement processes ensure that contracts comply with organizational policies, industry regulations, and legal requirements.</li>
      <li><strong>Stakeholder Confidence</strong> — A transparent procurement process builds trust with stakeholders by demonstrating fairness, accountability, and due diligence.</li>
    </ul>
  </section>

